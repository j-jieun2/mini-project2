package com.eomcs.pms.domain;

public class Player {
  private int no;
  private String name;
  private String stage;

  public int getNo() {
    return no;
  }
  public void setNo(int no) {
    this.no = no;
  }
  public String getName() {
    return name;
  }
  public void setName(String title) {
    this.name = title;
  }
  public String getStage() {
    return stage;
  }
  public void setStage(String stage) {
    this.stage = stage;
  }
}






